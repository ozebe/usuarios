    private void novaFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_novaFieldKeyReleased
        if (evt.getKeyCode() == KeyEvent.VK_ESCAPE) {
            UsuarioNovoView.definirSenhaBtn.setEnabled(true);
            this.dispose();
        } else if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            nova2Field.requestFocus();
        }
    }//GEN-LAST:event_novaFieldKeyReleased


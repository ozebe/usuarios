    private void okBtnKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_okBtnKeyReleased
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            try {
                if (verificaCamposSenha()) {
                    UsuarioNovoView.setSenha(MD5(senha));
                    UsuarioNovoView.salvarBtn.setEnabled(verificaCamposSenha());
                    this.dispose();
                }
            } catch (NoSuchAlgorithmException ex) {
                Logger.getLogger(DefinirSenhaView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_okBtnKeyReleased

    private Boolean verificaCamposSenha() throws NoSuchAlgorithmException {
        String nova1 = new String(novaField.getPassword()).trim();
        String nova2 = new String(nova2Field.getPassword()).trim();

        if (nova1.equals(nova2)) {
            if ((nova1.length() < 6)) {
                JOptionPane.showMessageDialog(null, "A nova senha deve ter no minimo 6 digitos!", "Erro", JOptionPane.ERROR_MESSAGE);
                return false;
            } else {
                this.senha = nova1;
                return true;
            }
        } else {
            JOptionPane.showMessageDialog(null, "Nova senha e confirmação não são as mesmas!", "Erro", JOptionPane.ERROR_MESSAGE);
            return false;
        }
    }

    private static String MD5(String s) throws NoSuchAlgorithmException {
        MessageDigest m = MessageDigest.getInstance("MD5");
        m.update(s.getBytes(), 0, s.length());
        return new BigInteger(1, m.digest()).toString(16);
    }

